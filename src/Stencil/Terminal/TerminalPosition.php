<?php

namespace Stencil\Terminal;

/**
 * 2D coordinates for terminal position.
 */
class TerminalPosition
{
    protected $x;
    protected $y;

    public function __construct($x, $y)
    {
        $this->x = $x;
        $this->y = $y;
    }

    public function getX()
    {
        return $this->x;
    }

    public function getY()
    {
        return $this->y;
    }
}